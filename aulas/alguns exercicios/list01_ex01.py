#  Soma basica entre quatro termos

num_um = int(input('Digite o primeiro valor: '))
num_dois = int(input('Digite o segundo valor: '))
num_tres = int(input('Digite o terceiro valor: '))
num_quatro = int(input('Digite o quarto valor: '))
soma = num_um + num_dois + num_tres + num_quatro
print(f'A soma dos quatro termos sendo eles: {num_um}, {num_dois}, {num_tres}, e {num_quatro} Ã©: {soma}')

